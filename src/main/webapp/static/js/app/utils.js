function VerifySslCertificate(server, fingerprint) {
    if (cordova.plugins.certificates) {
        cordova.plugins.certificates.trustUnsecureCerts(true);
    } else {
        alert("cordova.plugins.certificates not installed");
    }

    if ( window.plugins.sslCertificateChecker ) {
        window.plugins.sslCertificateChecker.check(
            successCertificateCallback,
            errorCertificateCallback,
            server,
            fingerprint);
    } else {
        alert("window.plugins.sslCertificateChecker not installed");
    }
};

